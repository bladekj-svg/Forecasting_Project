    "# =========================\n",
    "# Exponential Smoothing\n",
    "# =========================\n",
    "\n",
    "def fit_holt_winters(series, horizon):\n",
    "    model = ExponentialSmoothing(\n",
    "        series,\n",
    "        trend=None,\n",
    "        seasonal='add',\n",
    "        seasonal_periods=52\n",
    "    ).fit()\n",
    "\n",
    "    forecast = model.forecast(horizon)\n",
    "\n",
    "    resid_std = np.std(model.resid)\n",
    "\n",
    "    lower = forecast - 1.96 * resid_std\n",
    "    upper = forecast + 1.96 * resid_std\n",
    "\n",
    "    return model, forecast, lower, upper\n",
    "\n",
    "\n",
    "# =========================\n",
    "# ARIMA\n",
    "# =========================\n",
    "\n",
    "def fit_arima(series, order, horizon):\n",
    "    model = ARIMA(series, order=order).fit()\n",
    "\n",
    "    pred = model.get_forecast(steps=horizon)\n",
    "\n",
    "    forecast = pred.predicted_mean\n",
    "    conf_int = pred.conf_int()\n",
    "\n",
    "    lower = conf_int.iloc[:, 0]\n",
    "    upper = conf_int.iloc[:, 1]\n",
    "\n",
    "    return model, forecast, lower, upper\n",
    "\n",
    "\n",
    "# =========================\n",
    "# Random Forest\n",
    "# =========================\n",
    "\n",
    "def create_lags(series, n_lags=6):\n",
    "    df = pd.DataFrame({'y': series})\n",
    "\n",
    "    for i in range(1, n_lags + 1):\n",
    "        df[f'lag_{i}'] = df['y'].shift(i)\n",
    "\n",
    "    return df.dropna()\n",
    "\n",
    "\n",
    "\n",
    "def fit_random_forest(series, horizon):\n",
    "    df = create_lags(series)\n",
    "\n",
